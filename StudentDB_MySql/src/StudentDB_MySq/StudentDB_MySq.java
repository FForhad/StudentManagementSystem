    private void jbtnResultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnResultaActionPerformed

        int[] marks = new int[9];

        marks[0] = Integer.parseInt(jtxtScore1.getText());
        marks[1] = Integer.parseInt(jtxtScore2.getText());
        marks[2] = Integer.parseInt(jtxtScore3.getText());
        marks[3] = Integer.parseInt(jtxtScore4.getText());
        marks[4] = Integer.parseInt(jtxtScore5.getText());
        marks[5] = Integer.parseInt(jtxtScore6.getText());
        marks[6] = Integer.parseInt(jtxtScore7.getText());
        marks[7] = Integer.parseInt(jtxtScore8.getText());

        marks[8] = marks[0] + marks[1] + marks[2] + marks[3] + marks[4] + marks[5] + marks[6] + marks[7];
        jtxtTotalScore.setText(Integer.toString(marks[8]));

        if (marks[8] >= 600)
        {
            jtxtRanking.setText(" 1st class ");
        }

        else if (marks[8] >= 500)
        {
            jtxtRanking.setText(" 2nd class upor ");
        }

        else   if (marks[8] >= 400) 
        {
            jtxtRanking.setText(" 2nd class lower ");
        }

      else  if (marks[8] >= 300)
        {
            jtxtRanking.setText(" 3rd class ");
        }

          else  if (marks[8] >= 200)
        {
            jtxtRanking.setText("CoHE");
        }

       else if (marks[8] <= 199)
        {
            jtxtRanking.setText("Fail");
        }

        //marks 
        if (marks[0] > 100)
        {
            jtxtScore1.setText("0");
        } 
        else if (marks[1] > 100) 
        {
            jtxtScore2.setText("0");
        } 
        else if (marks[2] > 100)
        {
            jtxtScore3.setText("0");
        } 
        else if (marks[3] > 100)
        {
            jtxtScore4.setText("0");
        }
        else if (marks[4] > 100)
        {
            jtxtScore5.setText("0");
        }
        else if (marks[5] > 100) 
        {
            jtxtScore6.setText("0");
        } 
        else if (marks[6] > 100)
        {
            jtxtScore7.setText("0");
        }
        else if (marks[7] > 100)
        {
            jtxtScore8.setText("0");
        }

    }//GEN-LAST:event_jbtnResultaActionPerformed

